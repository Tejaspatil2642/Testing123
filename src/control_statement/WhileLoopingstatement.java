package control_statement;

public class WhileLoopingstatement {

	public static void main(String[] args) {
		// print 1 to 10 numbers
		int i=1;
		while(i<=10) {
		
			System.out.println(i);
			i++;
		}
		System.out.println("*****************************");
		int j =10;
		while(j>=1) {
			System.out.println(j);
			j--;
		}

	}

}
