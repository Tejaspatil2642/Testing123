package operator;

public class Relational_operator {

	public static void main(String[] args) {
	// Relational or comparison operator are 1) >  2) <  3) >=  4)<= 5)== 6) !=
		int a =50;
		double b = 60;
		System.out.println("To check a is greater than b :->"+(a>b));
		System.out.println("To check a is less than b :->"+(a<b));
		System.out.println("To check a is greater than or equals to b :->"+(a>=b));
		System.out.println("To check a is less than or equals to b :->"+(a<=b));
		System.out.println("To check a is equals to b :->"+(a==b));
		System.out.println("To check a is not equals to b :->"+(a!=b));
		
		
		
		
		
		
		

	}

}
