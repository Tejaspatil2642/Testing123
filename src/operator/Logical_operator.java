package operator;

public class Logical_operator {

	public static void main(String[] args) {

	//logical operator works between two boolean
		boolean x= true;
		boolean y= false;
		System.out.println("and operator between two values:"+(x&&y));
		System.out.println("or operator between two values"+(x || y));
		System.out.println("or operator between two values"+(!x));
		
		

	}

}
